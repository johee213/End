	//증감연산자ㅏ ++ --
		int num3 = 2;
		num3++; // num3 = num3 + 1  (num3 에 1을 더해서 num3에 담아라)  //3
		System.out.println(num3);  //3 찍고 엔터키 쳐라
		
		++num3; // num3 = num3 +1; //4
		System.out.println(num3); //4
		
		System.out.println(num3++); //5
		